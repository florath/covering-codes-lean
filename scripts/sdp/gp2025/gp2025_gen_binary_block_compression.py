        f"          Nat.choose ({n} - 2 * idx.k - interFree) (row - interFree) *",
        f"            Nat.choose ({n} - 2 * idx.k - row) (col - interFree) := by",
        "          apply Finset.sum_congr rfl",
        "          intro I hI",
        "          calc",
        f"            (({name_prefix}FreeSOnlyChoices idx I row interFree).sigma",
