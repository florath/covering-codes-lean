        f"/-- Positive scaling `2^k` for the length-{n} binary Schrijver blocks. -/",
        f"def {name_prefix}BlockScale (idx : BinaryBlockIndex {n}) : Rat :=",
        "  (2 : Rat) ^ idx.k",
        "",
        f"theorem {name_prefix}BlockScale_pos (idx : BinaryBlockIndex {n}) :",
        f"    0 < {name_prefix}BlockScale idx := by",
        f"  unfold {name_prefix}BlockScale",
        "  positivity",
        "",
        f"/-- Left coordinate of Schrijver pair `a` in block `k`. -/",
        f"def {name_prefix}PairLeftCoord",
        f"    (idx : BinaryBlockIndex {n}) (a : Fin idx.k) : Fin {n} :=",
        "  ⟨a.val, by",
        "    have ha := a.isLt",
        "    have hk := idx.hk",
        "    omega⟩",
        "",
        f"/-- Right coordinate of Schrijver pair `a` in block `k`. -/",
        f"def {name_prefix}PairRightCoord",
