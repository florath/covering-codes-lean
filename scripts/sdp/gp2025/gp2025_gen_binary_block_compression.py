        "  have hdisj : Disjoint I P := by",
        "    rw [Finset.disjoint_left]",
        "    intro coord hIcoord hPcoord",
        "    exact (Finset.mem_sdiff.mp (hP_subset hPcoord)).2 hIcoord",
